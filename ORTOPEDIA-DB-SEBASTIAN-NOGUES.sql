DROP DATABASE IF EXISTS Ortopedia_BM ;
CREATE DATABASE Ortopedia_BM ;

USE Ortopedia_BM ;

CREATE TABLE  PEDIDOS(
	IDPEDIDOS 		INT PRIMARY KEY AUTO_INCREMENT
,   IDCLIENTE 		INT
,   IDVENTAS        INT
,   IDSTOCK 	    INT
,   IDEMPLEADO 		INT
,   IDPROVEEDORES   INT
,   IDCOMPRAS       INT
,   FECHA 			DATETIME
,   CANCELACION 	DATETIME DEFAULT NULL
)
COMMENT 'Registra los pedidos realizadas por los clientes'
;

CREATE TABLE PROVEEDORES(
	IDPROVEEDORES   INT PRIMARY KEY AUTO_INCREMENT
,   NOMBRE          VARCHAR(100) DEFAULT 'PROVEEDOR'
,   TELEFONO        VARCHAR(10) NOT NULL
,   CORREO          VARCHAR(100) UNIQUE NOT NULL
,   CUIT            VARCHAR(11)
,   IDORTOPEDIA     INT
);

CREATE TABLE COMPRAS(
	IDCOMPRAS        INT PRIMARY KEY AUTO_INCREMENT
,   NOMBRE           VARCHAR(100) DEFAULT 'USUARIO'
,   TELEFONO         VARCHAR(10) 
,   CORREO           VARCHAR(100)  UNIQUE NOT NULL
,   CUIT             VARCHAR(11)  
,   IDORTOPEDIA      INT
,   IDPROVEEDORES    INT
);
   
CREATE TABLE  CLIENTE(
    IDCLIENTE 		INT PRIMARY KEY AUTO_INCREMENT
,   NOMBRE 			VARCHAR(100) DEFAULT 'PARTICULAR'
,   TELEFONO 		VARCHAR(20) NOT NULL
,   CORREO 			VARCHAR(100) UNIQUE NOT NULL
,   IDEMPLEADO      INT
);

CREATE TABLE  EMPLEADO(
	IDEMPLEADO		INT PRIMARY KEY AUTO_INCREMENT
,	NOMBRE			VARCHAR(100)
,	CORREO			VARCHAR(100) DEFAULT	'Ortopedia_BM@ventas.com'
,	TELEFONO		VARCHAR(100)
,	CUIT			VARCHAR(11)
,	IDORTOPEDIA	    INT
,   IDDUEÑO         INT
,   IDSTOCK         INT
,   IDCLIENTE       INT
);

CREATE TABLE  DUEÑO(
	IDDUEÑO			INT PRIMARY KEY AUTO_INCREMENT
,	NOMBRE			VARCHAR(100)
,	CORREO			VARCHAR(100) DEFAULT	'Ortopedia_BM@GerenteGeneral.com'
,	TELEFONO		VARCHAR(100)
,	CUIT			VARCHAR(11)
,   IDORTOPEDIA     INT
,   IDCOMPRAS       INT
);

CREATE TABLE  ORTOPEDIA(
	IDORTOPEDIA  	INT PRIMARY KEY AUTO_INCREMENT
,	NOMBRE			VARCHAR(100)
,   DIRECCION		VARCHAR(100)
,   TELEFONO		VARCHAR(100)
,   IDSTOCK         INT
);

CREATE TABLE  VENTAS(
	IDVENTAS	INT PRIMARY KEY AUTO_INCREMENT
,	ELEMENTO	VARCHAR(100)
,   NOMBRE      VARCHAR(100)
,   DIRECCION   VARCHAR(100)
,   TELEFONO    VARCHAR(100)
,   IDORTOPEDIA INT
);

CREATE TABLE  STOCK(
	IDSTOCK 		INT PRIMARY KEY AUTO_INCREMENT
,	IDORTOPEDIA	    INT
,	CANTIDAD		INT
,	DISPONIBILIDAD	BOOLEAN
)
COMMENT "Contiene información sobre el stock disponibles en cada Ortopedia"
;

